public class Mavenproject4 {

    public static void main(String[] args) {
    char ch='U';    
    switch(ch)  
    {  
        case 'A':   
            System.out.println("Albino");  
            break;  
        case 'B':   
            System.out.println("Blind");  
            break;  
        case 'C':   
            System.out.println("Crooked");  
            break;  
        case 'D':   
            System.out.println("Deaf");  
            break;  
        case 'U':   
            System.out.println("Autism");  
            break;    
        default:   
            System.out.println("Wrong Entry");  
    }  
}    
}
